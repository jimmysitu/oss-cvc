extern int __running_cvc_exe;
extern int __gbl_argc;
extern char **__gbl_argv;
extern unsigned long **__idpdat_areasp;
extern void (*__dsgn_initp)(void);

extern int __exe_args_num;
extern char *__exe_args_str[];

extern char *__exe_args_str[];
extern unsigned long *__idpdat_areas[];

extern void __dsgn_init(void);

extern int __dig_main(int, char **);

/*
 * the compiled executables main
 */
int main(int argc, char **argv)
{
 int rv1;

 /* mark executable flag to TRUE */
 __running_cvc_exe = 1;
 /* link in the orignal values for argc/argv */
 __gbl_argc = __exe_args_num;
 __gbl_argv = __exe_args_str;
 /* set the init design routine address */
 __dsgn_initp = __dsgn_init;
 /* link the fixed __idpdat areas */
 __idpdat_areasp = __idpdat_areas;
 rv1 = __dig_main(argc, argv);
 return(rv1);
}
